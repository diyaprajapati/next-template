const defaultUser: SidebarUser = {
  name: "User",
  email: "Loading…",
  avatar: "",
}

// Placeholder user used only for initial SSR + first client paint to avoid hydration mismatch.
const placeholderUser: SidebarUser = {
  name: "…",
  email: "…",
  avatar: "",
}

export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const [user, setUser] = React.useState<SidebarUser | null>(null)
  const [mounted, setMounted] = React.useState(false)

  React.useEffect(() => {
    setMounted(true)
  }, [])

  React.useEffect(() => {
    if (!mounted) return
    let cancelled = false
    fetch("/api/auth/me", { credentials: "include" })
      .then((res) => (res.ok ? res.json() : null))
      .then((data) => {
        if (cancelled || !data?.user) return
        const u = data.user
        setUser({
          name: u.name ?? u.email ?? "User",
          email: u.email ?? "",
          avatar: u.image ?? "",
        })
      })
      .catch(() => {})
    return () => {
      cancelled = true
    }
  }, [mounted])

  // Use a fixed placeholder until mounted so server and client render the same (avoids hydration error).
  const displayUser = mounted ? (user ?? defaultUser) : placeholderUser

  return (
    <Sidebar collapsible="icon" {...props}>
      <SidebarHeader>
        <TeamSwitcher teams={data.teams} />
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={data.navMain} />
        <NavProjects projects={data.projects} />
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={displayUser} />
      </SidebarFooter>
      <SidebarRail />
    </Sidebar>
  )
}
